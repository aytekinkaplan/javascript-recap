// Booleans Examples
let a = true;
let b = false;

console.log(a);
console.log(b);

console.log(a && b);
console.log(a || b);
console.log(!a);
console.log(!b);

let c = a && b;
console.log(c);

let d = a || b;
console.log(d);

let e = !a;
console.log(e);

let f = !b;
console.log(f);

let g = !c;
console.log(g);

let h = !d;
console.log(h);

let i = !e;
console.log(i);

let j = !f;
console.log(j);
let k = 40 > 20;
console.log(k);
let l = 40 < 20;
console.log(l);
let m = 40 >= 20;
console.log(m);
let n = 40 <= 20;
console.log(n);
let o = 40 == 20;
console.log(o);
let p = 40 != 20;
console.log(p);
let q = 40 === 20;
console.log(q);
let r = 40 !== 20;
console.log(r);
